// Store original English content
const englishContent = {};

function initI18n() {
  // Cache all original English text
  document.querySelectorAll('[data-i18n]').forEach(el => {
    englishContent[el.getAttribute('data-i18n')] = el.textContent;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    englishContent[el.getAttribute('data-i18n-html')] = el.innerHTML;
  });

  // Wire up language buttons
  document.querySelectorAll('[data-lang-select]').forEach(btn => {
    btn.addEventListener('click', () => {
      const lang = btn.getAttribute('data-lang-select');
      setLanguage(lang);

      // Toggle active class on buttons
      document.querySelectorAll('[data-lang-select]').forEach(b => b.classList.remove('active'));
      btn.classList.add('active');
    });
  });
}

function setLanguage(lang) {
  document.documentElement.setAttribute('data-lang', lang);
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';

  if (lang === 'en') {
    // Restore original English content
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (englishContent[key] !== undefined) {
        el.textContent = englishContent[key];
      }
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      if (englishContent[key] !== undefined) {
        el.innerHTML = englishContent[key];
      }
    });
  } else if (lang === 'pt' && translations.pt) {
    // Apply Portuguese translations
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (translations.pt[key]) {
        el.textContent = translations.pt[key];
      }
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      if (translations.pt[key]) {
        el.innerHTML = translations.pt[key];
      }
    });
  }

  // Update page title
  document.title = lang === 'pt'
    ? 'Hammer Consulting Group | Operações Globais de Cannabis e Estratégia Regulatória'
    : 'Hammer Consulting Group | Global Cannabis Operations & Regulatory Strategy';
}

